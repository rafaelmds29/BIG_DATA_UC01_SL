n1 = int(input('Insira o primeiro número: '))
n2 = int(input('Insira o segundo número: '))
if n1 == n2:
    print(f'Os números são iguais. O produto deles é igual a {n1 * n2}.') #o produto dentro das chaves não fica guardado em nenhum local
else:
    print(f'Os números são diferentes. A soma deles é igual a {n1 + n2}.')